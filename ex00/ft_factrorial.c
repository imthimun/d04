int ft_iterative_factorial(int nb)
{
	int fact;

	fact = 1;
	if(nb < 1)
	{
		return 0;
	}else
	{
		while(0 < nb)
		{
			fact *= nb;
			nb--;
		}
	}
return fact;
}

